package main

import (
	"fmt"
)

func main() {
	for i := 0; i <= 5; i++ {
		if i == 0 {
			fmt.Println("zero")
		} else if i == 1 {
			fmt.Println("one")
		} else if i == 2 {
			fmt.Println("two")
		} else if i == 3 {
			fmt.Println("three")
		} else if i == 4 {
			fmt.Println("four")
		} else if i == 5 {
			fmt.Println("five")
		}

	}
}
